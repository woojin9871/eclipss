package Day02.copy;

public class Ex07_RelationOperator {
	public static void main(String[] args) {
		
		// 비교 연산
		// - 크기 비교
		//	: >, <, >=, <= 와 같은 크기를 비교하는 연산자
		System.out.println(7 > 3);
		System.out.println(7 < 3);
		System.out.println(7 >= 3);
		System.out.println(7 <= 3);
		System.out.println();
		
		// - 등가 비교
		// : ==, != 와 같은 동일값 여부를 비교하는 연산자
		int a = 10;
		int b = 5;
		int c = 10;
		System.out.println(a == b);		
		System.out.println(a != b);		
		System.out.println(a == c);		
		System.out.println(a != c);
		System.out.println();
		
	}
	
}
