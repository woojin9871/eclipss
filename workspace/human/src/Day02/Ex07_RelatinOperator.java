package Day02;

public class Ex07_RelatinOperator {
	public static void main(String[] args) {
		
		// 비교 연산
		// - 크기 비교
		// : >, <, >=, <= 와 같은 크기를 비교하는 연산자
		System.out.println(7 > 3);
		System.out.println(7 < 3);
		System.out.println(7 >= 3);
		System.out.println(7 <= 3);
		System.out.println();
		
		// - 등가 비교
		// ==, != 와 같은 같은 값인지 여부를 비교하는 연산자
		int a = 10;
		int b = 5;
		int c = 10;
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a == c);
		System.out.println(a != c);
		System.out.println();
		

		
		
		
	}
}
